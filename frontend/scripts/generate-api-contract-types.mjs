import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..', '..');
const openApiPath = path.join(root, 'backend', 'openapi.json');
const outputPath = path.join(root, 'frontend', 'src', 'shared', 'types', 'api-contract.types.ts');

const spec = JSON.parse(fs.readFileSync(openApiPath, 'utf8'));
const paths = Object.keys(spec.paths ?? {}).sort();
const union = paths.map((item) => `  | '${item}'`).join('\n') || '  | never';

const content = `// Generated by frontend/scripts/generate-api-contract-types.mjs from backend/openapi.json.
// Do not edit by hand.

import type { ApiResponse } from './api.types';

export type ApiContractPath =
${union};

export const apiContractPaths = ${JSON.stringify(paths, null, 2)} as const satisfies readonly ApiContractPath[];

export type ApiContractResponse = ApiResponse<unknown>;
export type ApiContractResponses = Record<ApiContractPath, ApiContractResponse>;
`;

fs.writeFileSync(outputPath, content);
console.log(`Generated ${outputPath}`);
